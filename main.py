a=int(input())
b=int(input())
z=(input())
if z=="+":
    c=a+b
    print(c)
elif z=="-":
    c=a-b
    print(c)
elif z=="*":
    c=a*b
    print(c)
elif z=="/":
    c=a/b
    print(c)
else:
    print("posledniy vvedenniy simvol dolzhen byt znakom slozheniya,vychitaniya,umnozheniya ili deleniya")




